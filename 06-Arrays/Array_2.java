package Arrays;

public class Array_2 {
    public static void main(String[] args) {
        String[] arr = {"bell", "hell", "well", "sell", "dell"};
        System.out.println(arr[1]); //Printing only the ele at first index

        for(int j = 0 ; j < arr.length; j++){
            System.out.println(arr[j]); //Printing whole elements in array 
        }
    }
    
}
