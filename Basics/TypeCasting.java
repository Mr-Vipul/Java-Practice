public class TypeCasting {
    public static void main(String[] args){

        // Type Casting Explicitly
        float a  = 10 ;
        int b = (int)a ; 
        System.out.println("Value of b is : " + b);

        // Type Casting Implicitly
        int c  = 24 ; 
        float d = c ; 
        System.out.println("value is : " + d);
    } 
}
